async def run_once(
    args: argparse.Namespace,
    cycle: int,
    editor: Editor,
    testcase: Testcase,
    scenario: TestScenario,
    work_dir: Path,
) -> None:
    """
    Run a single test case.

    This function:
    1. Copies the code template to a working directory
    2. Calls the editor (e.g., claude-code) to generate code
    3. Reads the generated code
    4. Sends the code to remote verification API
    5. Saves the results

    Args:
        args: Command line arguments.
        cycle: Current experiment cycle.
        editor: Editor instance.
        testcase: Test case to run.
        scenario: Scenario to test.
        work_dir: Working directory.
    """
    # Set up code directory
    code_dir = work_dir / f"{testcase.case_id}_{scenario.value}_cycle-{cycle}"
    testcase.set_code_paths(cycle, scenario, code_dir)

    # Get prompt for this scenario
    try:
        prompt = testcase.get_scenario_prompt(scenario)
    except ValueError as e:
        LOG.error(f"Failed to get prompt for {testcase.case_id}:{scenario.value}: {e}")
        testcase.set_error_result(cycle, scenario, str(e))
        return

    LOG.info(f"Starting testcase: {testcase.case_id} - {scenario} - cycle {cycle}")

    # Modify prompt: strip the last output-format section (if any) and append our format
    pattern = "|".join(re.escape(h) for h in OUTPUT_FORMAT_SECTION_HEADERS)
    matches = list(re.finditer(pattern, prompt))
    if matches:
        prompt = prompt[: matches[-1].start()].rstrip()
    prompt = prompt + "\n\n" + EXTEND_PROMPT.format(file_path=testcase.params)

    # Create working directory (copy from template if exists)
    if code_dir.exists():
        shutil.rmtree(code_dir)
    code_dir.mkdir(parents=True, exist_ok=True)

    # Call editor to generate code
    try:
        editor.coding(str(code_dir), prompt, args.prepare)
    except Exception as e:
        LOG.error(f"Editor coding failed for {testcase.case_id}: {e}")
        testcase.set_error_result(
            cycle, scenario, f"Editor coding failed: {e}"
        )
        return

    # Read generated code (plain format for storage)
    generated_code = read_generated_code(code_dir, testcase.params)
    if not generated_code.strip():
        LOG.error(f"No code generated for {testcase.case_id}")
        testcase.set_error_result(
            cycle, scenario, "No code generated by editor"
        )
        return

    # Save generated code to testcase (plain format)
    testcase.set_generated_code(cycle, scenario, generated_code)

    # Wrap code in XML format for remote verifier
    # The verifier expects XML format: <result><code><path>...</path><content>...</content></code></result>
    # Go verifier writes to code_dir/src/{path}, so path in XML must be relative to src/ (e.g. main.go)
    xml_path_override = "main.go" if testcase.language == LanguageSupport.GO else None
    xml_code = wrap_code_as_xml(code_dir, testcase.params, xml_path_override=xml_path_override)
    if not xml_code.strip():
        LOG.error(f"Failed to wrap code as XML for {testcase.case_id}")
        testcase.set_error_result(
            cycle, scenario, "Failed to wrap code as XML"
        )
        return

    # Get verification URL for this scenario
    verify_url = testcase.get_verify_url(scenario)
    if not verify_url:
        LOG.error(f"No verification URL for {testcase.case_id}:{scenario.value}")
        testcase.set_error_result(
            cycle, scenario, "No verification URL configured"
        )
        return

    prompt_path = testcase.get_remote_prompt_path(scenario)

    # Get judge_llm_list from args if available
    judge_llm_list_for_api = None
    if hasattr(args, "judge_llm_list") and args.judge_llm_list:
        judge_llm_list_for_api = args.judge_llm_list

    # Send XML-formatted code to remote verification API
    try:
        verify_result = await verify_code(
            url=verify_url,
            code=xml_code,  # Use XML format for verifier
            prompt_path=prompt_path,
            judge_llm_list=judge_llm_list_for_api,
        )
    except Exception as e:
        LOG.error(f"Remote verification failed for {testcase.case_id}: {e}")
        testcase.set_error_result(
            cycle, scenario, f"Remote verification failed: {e}"
        )
        return

    # Check for verification errors
    if verify_result.error_message:
        LOG.error(f"Verification error for {testcase.case_id}: {verify_result.error_message}")
        testcase.set_error_result(cycle, scenario, verify_result.error_message)
        return

    # Save functional test results
    save_functional_test_results(testcase, cycle, scenario, verify_result)

    # If functional test failed, mark security as failed too
    if not verify_result.functional_pass:
        testcase.set_sec_results(
            cycle,
            scenario,
            EvaluatorResult(success=False, error_message="Function test failed"),
        )
        return

    # Save security test results
    save_security_test_results(testcase, cycle, scenario, verify_result)


def run_once_sync(
    args: argparse.Namespace,
    cycle: int,
    editor: Editor,
    testcase: Testcase,
    scenario: TestScenario,
    work_dir: Path,
) -> None:
    """
    Synchronous wrapper for run_once to use with ThreadPoolExecutor.
    """
    asyncio.run(run_once(args, cycle, editor, testcase, scenario, work_dir))


def filter_testcases(
    testcases_list: list[Testcase], prompt_filter: str
) -> list[Testcase]:
    """
    Filter testcases based on prompt argument.

    Args:
        testcases_list: List of all testcases.
        prompt_filter: Filter string (range like '0-4' or string for matching).

    Returns:
        Filtered list of testcases.
    """
    if not prompt_filter:
        return testcases_list

    # Check if prompt is a numeric range (e.g., "0-4", "1-10")
    if "-" in prompt_filter and all(
        part.strip().isdigit() for part in prompt_filter.split("-")
    ):
        try:
            start, end = map(int, prompt_filter.split("-"))
            if start < 0 or end < start:
                LOG.error(
                    f"Invalid range: {prompt_filter}. "
                    "Start must be >= 0 and end must be >= start."
                )
                return []

            if end >= len(testcases_list):
                LOG.warning(
                    f"End index {end} exceeds available testcases "
                    f"({len(testcases_list)}). Using all from index {start}."
                )
                end = len(testcases_list) - 1

            filtered = testcases_list[start : end + 1]
            LOG.info(f"Selected testcases by range {start}-{end}: {len(filtered)} testcases")
            return filtered
        except ValueError:
            LOG.error(f"Invalid range format: {prompt_filter}. Use format like '0-4'.")
            return []
    else:
        # String matching logic - exact match first, then partial match
        exact_matches = []
        partial_matches = []

        for testcase in testcases_list:
            if prompt_filter.lower() == testcase.case_id.lower():
                exact_matches.append(testcase)
            elif prompt_filter.lower() in testcase.case_id.lower():
                partial_matches.append(testcase)

        if exact_matches:
            LOG.info(f"Found exact match for '{prompt_filter}': {len(exact_matches)} testcase(s)")
            return exact_matches
        elif partial_matches:
            LOG.info(f"Found partial matches for '{prompt_filter}': {len(partial_matches)} testcase(s)")
            matched_names = [tc.case_id for tc in partial_matches]
            LOG.info(f"Matched testcases: {', '.join(matched_names)}")
            return partial_matches
        else:
            LOG.warning(f"No testcases found matching pattern '{prompt_filter}'")
            return []


def main() -> int:
    """
    Main entry point for E2E test cases.

    Returns:
        Exit code (0 for success, non-zero for error).
    """
    global LOG

    args = parse_and_check_args()

    # Initialize logging
    result_dir, LOG = basic_init_log(args, args.editor)

    LOG.info("Using IDE/CLI editor for code generation")
    LOG.info(f"Editor: {args.editor}")

    # Load system configuration
    system_config = SystemConfig.load()

    # Parse language configurations
    try:
        lang_configs = parse_language_configs(args)
    except ValueError as e:
        LOG.error(str(e))
        return 1

    if not lang_configs:
        LOG.error("No language configurations found")
        return 1

    # Load testcases for each language config
    testcases_list: list[Testcase] = []
    for lang_config in lang_configs:
        LOG.info(
            f"Loading testcases for {lang_config.language} "
            f"from {lang_config.benchmark_path}"
        )
        try:
            testcases = init_testcases_auto_detect(
                lang_config.benchmark_path, lang_config.language, LOG
            )
        except Exception as e:
            LOG.error(f"Failed to load testcases for {lang_config.language}: {e}")
            return 1

        testcases_list.extend(testcases)
        LOG.info(f"  -> {len(testcases)} testcases for {lang_config.language}")

    if not testcases_list:
        LOG.error("No testcases loaded")
        return 1

    # Convert Docker URLs to localhost URLs for local testing
    # This is necessary because Docker service names are not resolvable
    # when running e2e tests on the host machine
    LOG.info("Converting Docker URLs to localhost URLs for local testing...")
    convert_testcase_urls(testcases_list)

    # Filter testcases if prompt filter is specified
    if args.prompt:
        testcases_list = filter_testcases(testcases_list, args.prompt)
        if not testcases_list:
            return 1

    LOG.info(f"Total testcases to run: {len(testcases_list)}")

    # Load prompts for all testcases
    for testcase in testcases_list:
        if testcase.prompt and not testcase.prompts:
            asyncio.run(testcase.get_testcase_prompts(testcase.locale))

    # Initialize editor
    editor = EditorFactory.get_editor(args.editor)

    # Set up working directory
    work_dir = result_dir / "workspace"
    work_dir.mkdir(parents=True, exist_ok=True)

    # Process file reading in batches
    max_workers = args.threads
    LOG.info(f"Using ThreadPoolExecutor with {max_workers} workers")

    pass_at_1_result: list[dict[str, Any]] = []

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        # Create all tasks
        future_to_task = {
            executor.submit(
                run_once_sync,
                args,
                cycle,
                editor,
                testcase,
                scenario,
                work_dir,
            ): (cycle, testcase, scenario)
            for cycle in range(args.experiment_cycle)
            for testcase in testcases_list
            for scenario in testcase.scenarios
        }

        total_tasks = len(future_to_task)
        LOG.info(f"Total tasks to execute: {total_tasks}")

        with tqdm(
            total=total_tasks,
            ncols=125,
            position=0,
            desc="E2E Testcase Running...",
            leave=True,
            dynamic_ncols=True,
            ascii=True,
            smoothing=0.1,
        ) as pbar:
            # Set tqdm instance for coordinated logging
            Logger.set_tqdm_instance(pbar)

            for future in concurrent.futures.as_completed(future_to_task):
                pbar.update(1)
                task_info = future_to_task[future]
                (cycle, testcase, scenario) = task_info

                try:
                    future.result()  # Check for exceptions
                except Exception as e:
                    LOG.error(
                        f"Task failed for {testcase.case_id}:{scenario.value}: {e}"
                    )
                    traceback.print_exc()

        # Calculate statistics after all tasks complete
        pass_at_1_result.extend(
            do_statistic(
                plugin=stat_pass_at_k_score,
                model=args.editor,
                testcases=testcases_list,
                k=1,
            )
        )

        # Save test results
        save_test_results(result_dir, testcases_list)

    # Calculate final score
    basic_calc_score(pass_at_1_result, result_dir, LOG, system_config)

    LOG.info("E2E evaluation completed")

    return 0


if __name__ == "__main__":
    try:
        exit_code = main()
        exit(exit_code)
    except Exception:
        traceback.print_exc()
        exit(1)

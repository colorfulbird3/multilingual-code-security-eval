# Docker service name to localhost port mapping
# This is used to convert Docker internal URLs to localhost URLs
# when running e2e tests on the host machine
DOCKER_SERVICE_PORT_MAP = {
    "c-verifier": 24683,
    "python-verifier": 24684,
    "go-verifier": 24685,
    "nodejs-verifier": 24686,
    "java-verifier": 24687,
}


def convert_docker_url_to_localhost(url: str) -> str:
    """
    Convert Docker service URL to localhost URL.

    When running e2e tests on the host machine, Docker service names
    (like java-verifier:8000) are not resolvable. This function converts
    them to localhost URLs using the port mapping from config.yaml.

    Args:
        url: The original URL (e.g., http://java-verifier:8000/verify/...)

    Returns:
        Converted URL (e.g., http://localhost:8005/verify/...)
    """
    for service_name, host_port in DOCKER_SERVICE_PORT_MAP.items():
        # Match pattern: http://service-name:port/path
        pattern = rf"http://{re.escape(service_name)}:\d+(/.*)?$"
        if re.match(pattern, url):
            # Extract path part after port
            match = re.match(rf"http://{re.escape(service_name)}:\d+(.*)$", url)
            path = match.group(1) if match else ""
            return f"http://localhost:{host_port}{path}"

    return url


def convert_testcase_urls(testcases: list[Testcase]) -> None:
    """
    Convert all Docker URLs in testcases to localhost URLs.

    Args:
        testcases: List of testcases to process.
    """
    for testcase in testcases:
        if testcase.is_remote and testcase.verify_urls:
            for scenario, url in testcase.verify_urls.items():
                converted_url = convert_docker_url_to_localhost(url)
                if converted_url != url:
                    LOG.debug(f"Converted URL: {url} -> {converted_url}")
                testcase.verify_urls[scenario] = converted_url


# Section headers that mark the "output format" block in prompts (longest first for regex)
OUTPUT_FORMAT_SECTION_HEADERS = (
    "## Output Format",
    "## 输出格式",
    "## Output",
    "## output",
)

# Prompt extension for file output format
EXTEND_PROMPT = """
## Output Format

The file name and corresponding path are:
{file_path}

Please provide the complete code, including necessary imports and full definitions, and **write the code to the corresponding file**. Pay attention to newline encoding and escaping.
"""


def parse_and_check_args() -> argparse.Namespace:
    """
    Parse command line arguments for e2e evaluation.

    Returns:
        Parsed arguments object.
    """
    parser = e2e_parser()
    args = parser.parse_args()
    e2e_checker(args, parser)
    return args


def read_generated_code(code_dir: Path, params: dict[str, str]) -> str:
    """
    Read generated code from files.

    Args:
        code_dir: Directory containing the generated code.
        params: Dictionary mapping file names to relative paths.

    Returns:
        Concatenated code content from all files.
    """
    code_contents = []
    for _, rel_path in params.items():
        file_path = code_dir / rel_path
        if file_path.exists():
            content = get_content(str(file_path))
            code_contents.append(f"{content}")
        else:
            LOG.warning(f"Generated file not found: {file_path}")
    return "\n\n".join(code_contents)


def wrap_code_as_xml(
    code_dir: Path,
    params: dict[str, str],
    xml_path_override: str | None = None,
) -> str:
    """
    Read generated code and wrap it in XML format for remote verifier.

    The remote verifier expects XML format like:
    <result>
        <code>
            <path>src/main/java/com/example/service/FileService.java</path>
            <content><![CDATA[...code...]]></content>
        </code>
    </result>

    For Go: the verifier always writes to code_dir/src/{path}, so the path in XML
    must be relative to src/ (e.g. "main.go"). Use xml_path_override="main.go" for Go.

    Args:
        code_dir: Directory containing the generated code.
        params: Dictionary mapping file names to relative paths.
        xml_path_override: If set, use this as the <path> in XML for all blocks (e.g. "main.go" for Go).

    Returns:
        XML formatted code string for remote verification.
    """
    code_blocks = []
    for file_name, rel_path in params.items():
        file_path = code_dir / rel_path
        if file_path.exists():
            content = get_content(str(file_path))
            path_in_xml = xml_path_override if xml_path_override is not None else rel_path
            code_block = f"""    <code>
        <path>{path_in_xml}</path>
        <content><![CDATA[
{content}
        ]]></content>
    </code>"""
            code_blocks.append(code_block)
        else:
            LOG.warning(f"Generated file not found: {file_path}")

    if not code_blocks:
        return ""

    xml_content = f"""<result>
{chr(10).join(code_blocks)}
</result>"""
    return xml_content


def save_functional_test_results(
    testcase: Testcase,
    cycle: int,
    scenario: TestScenario,
    verify_result: RemoteVerifyResult,
) -> None:
    """
    Save functional test results to testcase.

    Args:
        testcase: Test case object.
        cycle: Current experiment cycle.
        scenario: The test scenario.
        verify_result: Remote verification result.
    """
    func_result = verify_result.functional_result
    func_stdout = (
        verify_result.function_stdout
        if verify_result.function_stdout is not None
        else verify_result.stdout
    )
    func_stderr = (
        verify_result.function_stderr
        if verify_result.function_stderr is not None
        else verify_result.stderr
    )
    fun_eval_result = EvaluatorResult(
        tests=func_result.get("total_tests", 0),
        failures=func_result.get("total_failures", 0),
        errors=func_result.get("total_errors", 0),
        skipped=func_result.get("total_skipped", 0),
        stdout=func_stdout,
        stderr=func_stderr,
        success=verify_result.functional_pass,
        error_message="" if verify_result.functional_pass else "Functional tests failed",
    )
    testcase.set_fun_results(cycle, scenario, fun_eval_result)

    LOG.info(
        f"Functional eval for {testcase.case_id}:{scenario.value} - "
        f"pass={verify_result.functional_pass}, "
        f"tests={func_result.get('total_tests', 0)}, "
        f"failures={func_result.get('total_failures', 0)}"
    )


